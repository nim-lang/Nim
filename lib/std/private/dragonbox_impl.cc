// Returns whether value is divisible by 2^e2
static inline bool MultipleOfPow2(uint64_t value, int32_t e2)
{
    DRAGONBOX_ASSERT(e2 >= 0);
    return e2 < 64 && (value & ((uint64_t{1} << e2) - 1)) == 0;
}

